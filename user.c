void main() {
    int val;
    int status;

    // 1. 初始化测试：写入数字 1 (绕过大数加载问题)
    // 如果烧录后显示 00000001，说明 CPU 和硬件写入正常
    $0xFFFFFC00 = 1; 

    while(1) {
        val = $0xfffffc10;    // 读键值
        status = $0xfffffc12; // 读状态
        
        // 2. 状态检测：如果有按键按下，将显示改为 2 + 键值
        // 这样可以区分“初始状态(1)”和“按键状态(2...)”
        if (status != 0) {
            $0xFFFFFC00 = 2 + val; // 如果按 '0'，显示 2；按 '1'，显示 3
        }
    }
}